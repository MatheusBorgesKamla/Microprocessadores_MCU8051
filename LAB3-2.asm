	ORG 0
	SJMP INI
	
	ORG 0003H
	SJMP INTE
	
INI:	CLR 25H.0
	SETB EA
	SETB EX0
	SETB IT0
	
PROG:	JB 25H.0, HZ2 

HZ1:	JB P3.0, UM1
	SETB P3.0
	ACALL DELAY1
	CLR P3.0
	ACALL DELAY1
	SJMP PROG
UM1:	CLR P3.0
	ACALL DELAY1
	SETB P3.0
	ACALL DELAY1
	SJMP PROG

HZ2:	JB P3.0, UM2
	SETB P3.0
	ACALL DELAY2
	CLR P3.0
	ACALL DELAY2
	SJMP PROG
UM2:	CLR P3.0
	ACALL DELAY2
	SETB P3.0
	ACALL DELAY2
	SJMP PROG

INTE:	CLR EA
	CPL 25H.0
	SETB EA
	RETI

DELAY1:	MOV R2, #62H
LOOP1:	MOV R1, #0AH
LOOP:	MOV R0, #0FEH
	DJNZ R0, $
	DJNZ R1, LOOP
	DJNZ R2, LOOP1
	RET

DELAY2:	MOV R2, #19H
LOOP3:	MOV R1, #0AH
LOOP2:	MOV R0, #0F9H
	DJNZ R0, $
	DJNZ R1, LOOP2
	DJNZ R2, LOOP3
	RET
	
